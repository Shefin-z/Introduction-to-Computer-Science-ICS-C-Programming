#include <stdio.h>

int main() {
    // Declare and initialize variables
    int integerVariable = 17;
    float floatVariable = 3.508;
    char charVariable = 'W';

    // Print the values
    printf("Integer Variable: %d\n", integerVariable);
    printf("Float Variable: %.3f\n", floatVariable);
    printf("Character Variable: %c\n", charVariable);

    return 0;
}

